package factoryMethod.kits;

public class Cuaderno {

    private String tipo;
    private int nroHojas;

    public Cuaderno() {}

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public int getNroHojas() {
        return nroHojas;
    }

    public void setNroHojas(int nroHojas) {
        this.nroHojas = nroHojas;
    }
}
